// footer accordion
$(".footer-accordion-header-1").click(function(e) {
  if ($(".footer-accordion-header-icon-1").hasClass("transform-left")) {
    $(".footer-accordion-header-icon-1").removeClass("transform-left");
    $(".footer-accordion-header-icon-1").addClass("transform-down");
  } else {
    $(".footer-accordion-header-icon-1").removeClass("transform-down");
    $(".footer-accordion-header-icon-1").addClass("transform-left");
  }
});

